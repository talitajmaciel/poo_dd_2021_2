// JavaScript
// O FOR tem 3 Campos:
// 1º é o que você Inicializa/Declara a sua Variável => declarei o "i", começando com o valor = 0
// 2º é a Condição de Continuação => Enquanto o "i" for Menor < 10, eu vou rodando esse For
// 3º é o Incremento
for(let i = 0; i < 10; i++){
    console.log(i);
    // Foi do 0 ao 9, no momento que Incrementou pro 10 essa Condição deixou de ser verdadeira e ele parou de fazer Iteração

}

// FOR Tipado no TS
// Faz do mesmo modo e se coloca o Tipo de Variável:
for(let i:number = 0; i < 20; i++){
    console.log(i);
    // O mesmo raciocínio, um for de 0 a 19.
}